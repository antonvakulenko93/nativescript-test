import Verida from '@verida/datastore';

const VUE_APP_VERIDA_APP_NAME = 'Verida Vault';
const VUE_APP_VERIDA_ENVIRONMENT = 'testnet';
const VUE_APP_VERIDA_SCHEMAS_BASE_PATH = undefined;

const CHAIN = 'ethr';

export async function connectVerida() {
  const web3Provider = await Verida.Helpers.wallet.connectWeb3(CHAIN)
  const address = await Verida.Helpers.wallet.getAddress(CHAIN)

  Verida.setConfig({
    appName: VUE_APP_VERIDA_APP_NAME,
    environment: VUE_APP_VERIDA_ENVIRONMENT,
    schemas: {
      basePath: VUE_APP_VERIDA_SCHEMAS_BASE_PATH,
    },
  });

  return new Verida({
    address: address,
    chain: CHAIN,
    web3Provider: web3Provider,
  });
}

